  static int getMaxLength(
    int typeNumber,
    QrCodeDataType dataType,
    ErrorCorrectionLevel errorCorrectionLevel,
  ) => _maxLength[typeNumber - 1][dataType.index][errorCorrectionLevel.index];

  static Polynomial getErrorCorrectPolynomial(int errorCorrectLength) {
    var a = Polynomial([1]);
    for (var i = 0; i < errorCorrectLength; i++) {
      a = a.multiply(Polynomial([1, QrMath.gexp(i)]));
    }
    return a;
  }

  static bool getMask(MaskPattern maskPattern, int i, int j) {
    switch (maskPattern) {
      case MaskPattern.pattern000:
        return (i + j) % 2 == 0;
      case MaskPattern.pattern001:
        return i % 2 == 0;
      case MaskPattern.pattern010:
        return j % 3 == 0;
      case MaskPattern.pattern011:
        return (i + j) % 3 == 0;
      case MaskPattern.pattern100:
        return (i ~/ 2 + j ~/ 3) % 2 == 0;
      case MaskPattern.pattern101:
        return (i * j) % 2 + (i * j) % 3 == 0;
      case MaskPattern.pattern110:
        return ((i * j) % 2 + (i * j) % 3) % 2 == 0;
      case MaskPattern.pattern111:
        return ((i * j) % 3 + (i + j) % 2) % 2 == 0;
    }
  }

  static QrCodeDataType getDataType(String s) {
    if (_isAlphaNum(s)) {
      if (_isNumber(s)) {
        return QrCodeDataType.numbers;
      }
      return QrCodeDataType.upperAlphaNum;
    }
    return QrCodeDataType.defaultType;
  }

  static bool _isNumber(String s) => RegExp(r'^\d+$').hasMatch(s);
  static bool _isAlphaNum(String s) => RegExp(r'^[0-9A-Z $%*+\-./:]+$').hasMatch(s);

  static const int _g15 =
      (1 << 10) | (1 << 8) | (1 << 5) | (1 << 4) | (1 << 2) | (1 << 1) | (1 << 0);
  static const int _g18 =
      (1 << 12) | (1 << 11) | (1 << 10) | (1 << 9) | (1 << 8) | (1 << 5) | (1 << 2) | (1 << 0);
  static const int _g15Mask = (1 << 14) | (1 << 12) | (1 << 10) | (1 << 4) | (1 << 1);

  static int getBchTypeInfo(int data) {
    var d = data << 10;
    while (_getBchDigit(d) - _getBchDigit(_g15) >= 0) {
      d = d ^ (_g15 << (_getBchDigit(d) - _getBchDigit(_g15)));
    }
    return (data << 10 | d) ^ _g15Mask;
  }

  static int getBchTypeNumber(int data) {
    var d = data << 12;
    while (_getBchDigit(d) - _getBchDigit(_g18) >= 0) {
      d = d ^ (_g18 << (_getBchDigit(d) - _getBchDigit(_g18)));
    }
    return data << 12 | d;
  }

  static int _getBchDigit(int data) {
    var i = data;
    var digit = 0;
    while (i != 0) {
      digit++;
      i = i >>> 1;
    }
    return digit;
  }
